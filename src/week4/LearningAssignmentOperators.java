package week4;

public class LearningAssignmentOperators {

	public static void main(String[] args) {
		// Add & Assignment
		int a = 10;
		a += 15; // Same as a = a + 15
		System.out.println("a is " + a);
		// Subtract & Assignment
		int b = 10;
		b -= 15; // Same as b = b - 15
		System.out.println("b is " + b);
		// Multiply & Assignment
		int c = 10;
		c *= 3; // Same as c = c * 3
		System.out.println("c is " + c);
		// Division & Assignment
		int d = 10;
		d /= 5; // Same as d = d / 5
		System.out.println("d is " + d);
	}
	
}
