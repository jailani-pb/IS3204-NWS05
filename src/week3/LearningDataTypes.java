package week3;

public class LearningDataTypes {

	public static void main(String[] args) {
		byte exampleByte = 127;
		short exampleShort = 30000;
		int exampleInt = 1000000;
		long exampleLong = 923337203685477580L;
		System.out.println("The value inside exampleByte is " + exampleByte);
		System.out.println("The value inside exampleShort is " + exampleShort);
		System.out.println("The value inside exampleInt is " + exampleInt);
		System.out.println("The value inside exampleLong is " + exampleLong);
		float exampleFloat = 3.1234567890123456789012345678901234567890f;
		double exampleDouble = 3.1234567890123456789012345678901234567890d;
		System.out.println("The value inside exampleFloat is " + exampleFloat);
		System.out.println("The value inside exampleDouble is " + exampleDouble);
		char exampleChar = 'A';
		System.out.println("The value inside exampleChar is " + exampleChar);
		boolean exampleBoolean = false;
		System.out.println("The value inside exampleBoolean is " + exampleBoolean);
		
		System.out.println(exampleChar + 1);
		System.out.println((char)(exampleChar + 1));
		char exampleChar2 = 107;
		System.out.println(exampleChar2);
	}
	
}
